on tb_EDITORA
for each row
DECLARE VOPERACAO VARCHAR2(100);

BEGIN
    IF INSERTING THEN
        VOPERACAO:='NOVA EDITORA -'||:NEW.DESCRICAO;
    ELSE
      IF DELETING THEN 
         VOPERACAO:='EDITORA EXCLUÍDA -'||:OLD.DESCRICAO;
      ELSE
        IF UPDATING('ENDERECO') THEN
           VOPERACAO:='ENDERCO ALTERADO -'||:NEW.ENDERECO||'-'||:OLD.ENDERECO;
        ELSE
           VOPERACAO:='EDITORA ALTERADA -'||:NEW.ENDERECO||'-'||:OLD.ENDERECO;
        END IF;
      END IF;
    END IF;
    INSERT INTO TB_LOG VALUES (SQ_LOG.NEXTVAL,USER,SYSDATE,VOPERACAO);
 END;   
     
